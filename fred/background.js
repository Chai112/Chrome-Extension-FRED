function toHex (i)
{
  var o = Math.round(i).toString(16);
  while (o.length < 2) {o = '0' + o;}
  return o;
}

function drawLine (ooo, x, y)
{
  var relX = Math.abs(x.x - y.x);
  var relY = Math.abs(x.y - y.y);
  var d = Math.sqrt((relX*relX)+(relY*relY));
  for (var i = 0; i < d; i++)
  {
    if ((relX / d) * i % 2 === 1)
      relX = relX + 1;
    if ((relY / d) * i % 2 === 1)
      relY = relY + 1;

    ooo[Math.round(((relX / d) * i) / 2) * 2 + 2 + x.x][Math.round(((relY / d) * i) / 2) * 2 + 2 + x.y] = {r:255, g:0, b:0}
  }
  return ooo;
}

